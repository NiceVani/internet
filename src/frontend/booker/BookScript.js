        roomElement.classList.remove("available", "disabled-room");
        roomElement.style.backgroundColor = "#ff0000"; // üî¥ ‡∏™‡∏µ‡πÅ‡∏î‡∏á‡∏ó‡∏±‡πâ‡∏á‡∏´‡πâ‡∏≠‡∏á
