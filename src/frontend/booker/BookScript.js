          roomElement.classList.add("disabled-room");
          roomElement.classList.remove("available", "no-data");
          statusElement.textContent = "‡πÑ‡∏°‡πà‡∏ß‡πà‡∏≤‡∏á";
          statusElement.style.backgroundColor = "red"; // ‚ùå ‡∏™‡∏µ‡πÅ‡∏î‡∏á‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏õ‡πâ‡∏≤‡∏¢
